package sourcesoft.proassur;

public class SignUpData 
{
	
	private String firstName;
    private String lastName;
    private String password;
    private String confirmPassword;
    private String mobilePhone;
    private String mobile_code;
    private String email;
    private String enter_location;
	public String getFirstName() 
	{
		return firstName;
	}
	public void setFirstName(String firstName)
	{
		this.firstName = firstName;
	}
	public String getLastName()
	{
		return lastName;
	}
	public void setLastName(String lastName) 
	{
		this.lastName = lastName;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password)
	{
		this.password = password;
	}
	public String getConfirmPassword()
	{
		return confirmPassword;
	}
	public void setConfirmPassword(String confirmPassword)
	{
		this.confirmPassword = confirmPassword;
	}
	public String getMobilePhone() 
	{
		return mobilePhone;
	}
	public void setMobilePhone(String mobilePhone)
	{
		this.mobilePhone = mobilePhone;
	}
	public String getMobile_code()
	{
		return mobile_code;
	}
	public void setMobile_code(String mobile_code) 
	{
		this.mobile_code = mobile_code;
	}
	public String getEmail() 
	{
		return email;
	}
	public void setEmail(String email)
	{
		this.email = email;
	}
	public String getEnter_location()
	{
		return enter_location;
	}
	public void setEnter_location(String enter_location) 
	{
		this.enter_location = enter_location;
	}

	

}
